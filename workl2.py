C = float(input("กรอกค่าอุณหภูมิ (องศาเซลเซียส): "))
F = (9/5) * C + 32
K = C + 273.15
print(f"อุณหภูมิในหน่วยฟาเรนไฮต์: {F:.2f} °F")
print(f"อุณหภูมิในหน่วยเคลวิน: {K:.2f} K")
