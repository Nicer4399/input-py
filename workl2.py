print("a = {10, 20, 30, 40, 50,60}\nb = {30, 40, 50, 60, 70,80}\nนักเรียนเขียนคำสั่งด้วยภาษาไบทอนหาเซตของที่กำหนดให้แสดงผลตามที่กำหนด {70, 40, 10, 80, 50, 20, 60, 30} {40, 50, 60, 30} {10, 20} {80, 20, 70, 10}")

a = {10, 20, 30, 40, 50, 60}
b = {30, 40, 50, 60, 70, 80}
result1 = a.union(b)  # รวมทุกค่าในเซต a และ b
result2 = a.intersection(b)  # ค่าที่อยู่ในทั้ง a และ b
result3 = a.difference(b)  # ค่าที่อยู่ใน a แต่ไม่อยู่ใน b
result4 = a.symmetric_difference(b)  # ค่าที่อยู่ใน a หรือ b แต่ไม่อยู่ในทั้งคู่
print()
